    private void lerArquivo(String filename) {                
        Contribuinte contribuinte;
        DeclaracaoCompleta declaracaoCompleta;
        CadastroDeclaracao cadastroDeclaracao = new CadastroDeclaracao();
        try {
            Scanner sc = new Scanner(new File(filename));
            sc.useDelimiter(";");
            while (sc.hasNext()) {
                String nome = sc.next();
                System.out.println(nome);
                String cpf = sc.next();
                System.out.println(cpf);
                int idade = sc.nextInt();
                System.out.println(idade);
                int numDepen = sc.nextInt();
                System.out.println(numDepen);
                double totalRend = Double.parseDouble(sc.next());
                System.out.println(totalRend);                
                double contriOficial = Double.parseDouble(sc.next());;                
                System.out.println(contriOficial);
                contribuinte = new Contribuinte(nome, cpf, idade, 
                        numDepen, totalRend, contriOficial);
                declaracaoCompleta = new DeclaracaoCompleta(contribuinte);
                cadastroDeclaracao.incluirDeclaracao(declaracaoCompleta);                
            }
            sc.close();
            System.out.println(cadastroDeclaracao.toString());
        } catch (IOException ioe) {
            JOptionPane.showMessageDialog(null,
                    "Arquivo n√£o encontrado!",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }        
    }

